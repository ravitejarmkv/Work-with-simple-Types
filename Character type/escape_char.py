# \' for a single quote.
singleQuote = '\''

# \" for double quote.
doubleQuote = '\"'

# \\ for backslash.
backSlash = '\\'

# \0 for null character.
nullCharacter = '\0'

# \a for alert character.
alertCharacter = '\a'

# \b for backspace.
backSpace = '\b'

# \n for newline.
newLine = '\n'

# /r for a carriage return.
carriageReturn = '\r'

# \v for vertical tab.
verticalTab = '\v'

# \t for horizontal tab.
horizontalTab = '\t'

# \x for unicode character hex value.
unicodeCharacter = '\x41'

print(singleQuote)
print(doubleQuote)
print(backSlash)
print(nullCharacter)
print(alertCharacter)
print(backSpace)
print(newLine)
print(carriageReturn)
print(verticalTab)
print(horizontalTab)
print(unicodeCharacter)
